class OpenAIService {
    
    public function chat($systemPrompt, $history, $userMessage) {
        // Демо-режим: возвращаем заранее подготовленные ответы
        $responses = [
            'привет' => 'Здравствуйте! Рада вас видеть. Как прошёл ваш день?',
            'как дела' => 'Спасибо за вопрос! Я здесь, чтобы поддержать вас. Расскажите, что у вас происходит?',
            'стресс' => 'Стресс — это естественная реакция организма. Попробуйте сделать несколько глубоких вдохов. Хотите, предложу упражнение?',
            'тревога' => 'Тревога может быть очень неприятной. Помните, что это временное состояние. Попробуйте упражнение "5-4-3-2-1": назовите 5 предметов вокруг, 4 звука, 3 тактильных ощущения, 2 запаха и 1 вкус.',
            'спасибо' => 'Пожалуйста! Я всегда рада помочь. Если хотите, могу предложить медитацию или упражнение.',
            'психолог' => 'Если вы хотите поговорить с живым психологом, вы можете записаться на консультацию в разделе "Психологи".',
            'дефолт' => 'Спасибо, что делитесь. Это важно. Хотите выполнить короткое упражнение для снятия напряжения?'
        ];
        
        $lowerMessage = mb_strtolower($userMessage);
        
        foreach ($responses as $key => $response) {
            if (mb_strpos($lowerMessage, $key) !== false) {
                return $response;
            }
        }
        
        return $responses['дефолт'];
    }
}